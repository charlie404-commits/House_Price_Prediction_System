"""
utils/report_generator.py
--------------------------
Generates a simple, professional single-prediction PDF report using fpdf2.

Kept deliberately separate from prediction logic — this module only
formats already-computed results, it never calls the model.
"""

from __future__ import annotations

from typing import Any, Dict

from fpdf import FPDF

from config import APP_NAME, APP_VERSION
from utils.formatting import format_inr, humanize_timestamp


LABELS = {
    "area": "Area (sq ft)",
    "bedrooms": "Bedrooms",
    "bathrooms": "Bathrooms",
    "stories": "Stories",
    "mainroad": "Main Road",
    "guestroom": "Guest Room",
    "basement": "Basement",
    "hotwaterheating": "Hot Water Heating",
    "airconditioning": "Air Conditioning",
    "parking": "Parking",
    "prefarea": "Preferred Area",
    "furnishingstatus": "Furnishing Status",
}


def build_prediction_pdf(record: Dict[str, Any]) -> bytes:
    """Build a one-page PDF summarizing a single prediction record."""
    pdf = FPDF(unit="mm", format="A4")
    pdf.add_page()
    pdf.set_auto_page_break(auto=True, margin=18)

    # Header
    pdf.set_fill_color(11, 15, 25)
    pdf.rect(0, 0, 210, 32, style="F")
    pdf.set_text_color(255, 255, 255)
    pdf.set_font("Helvetica", "B", 18)
    pdf.set_xy(12, 10)
    pdf.cell(0, 10, f"{APP_NAME} - Prediction Report", ln=True)
    pdf.set_font("Helvetica", "", 10)
    pdf.set_xy(12, 20)
    pdf.cell(0, 6, f"House Price Prediction System | v{APP_VERSION}", ln=True)

    pdf.ln(20)
    pdf.set_text_color(20, 20, 20)

    # Predicted price highlight
    pdf.set_font("Helvetica", "B", 14)
    pdf.cell(0, 10, "Estimated Price", ln=True)
    pdf.set_font("Helvetica", "B", 22)
    pdf.set_text_color(232, 163, 61)
    pdf.cell(0, 12, format_inr(record.get("predicted_price", 0)), ln=True)
    pdf.set_text_color(20, 20, 20)

    pdf.set_font("Helvetica", "", 10)
    pdf.cell(0, 8, f"Generated: {humanize_timestamp(record.get('timestamp', ''))}", ln=True)
    pdf.ln(6)

    # Input summary table
    pdf.set_font("Helvetica", "B", 13)
    pdf.cell(0, 8, "Property Details", ln=True)
    pdf.ln(2)

    pdf.set_font("Helvetica", "", 11)
    for key, label in LABELS.items():
        if key not in record:
            continue
        pdf.set_font("Helvetica", "B", 11)
        pdf.cell(60, 8, label, border=0)
        pdf.set_font("Helvetica", "", 11)
        pdf.cell(0, 8, str(record[key]), ln=True)

    pdf.ln(8)
    pdf.set_font("Helvetica", "I", 8)
    pdf.set_text_color(120, 120, 120)
    pdf.multi_cell(
        0, 5,
        "This estimate is generated by a Machine Learning model trained on "
        "historical housing data. It is intended as a reference only and "
        "does not constitute a professional property valuation.",
    )

    return bytes(pdf.output(dest="S"))
